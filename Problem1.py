def compare_inputs():
    # Get input from the user
    input1 = input("Enter the first value: ")
    input2 = input("Enter the second value: ")

    # Compare inputs and print the result
    if input1 == input2:
        print("The inputs are equal.")
    else:
        print("The inputs are not equal.")


# Call the function
compare_inputs()
