def check_for_value_5(input_list):
    # Checks if the value 5 is in the list
    if 5 in input_list:
        print("The value 5 is in the list.")
    else:
        print("The value 5 is not in the list.")


# Example usage:
my_list = [1, 3, 5, 7, 9]

# Call the function with the example list
check_for_value_5(my_list)